<template>
  <div class="b-table-wrapper">
    <table class="b-table" :class="{ bordered, tight, striped }">
      <thead>
        <tr>
          <th><input ref="selectAll" @change="onSelectedAllItems" type="checkbox" :checked="areAllItemsSelected" /></th>
          <th v-if="indexIsVisable">#</th>
          <th v-for="col in columns" :key="col.field">
            <div class="b-table-header">
              {{ col.text }}
              <span class="b-table-sorter" v-if="col.field in orderBy" @click="changeOrderBy(col.field)">
                <b-icon name="asc" :class="{ active: orderBy[col.field] === 'asc' }"></b-icon>
                <b-icon name="desc" :class="{ active: orderBy[col.field] === 'desc' }"></b-icon>
              </span>
            </div>
          </th>
        </tr>
      </thead>
      <tbody>
        <tr v-for="(row, index) in dataSource" :key="row.id">
          <!-- checked 根据selectedItems设置选中状态 -->
          <th><input type="checkbox" @change="onSlecteItem(row, index, $event)" :checked="inSelectedItems(row)" /></th>
          <td v-if="indexIsVisable">{{ index }}</td>
          <template v-for="col in columns">
            <td :key="col.field">{{ row[col.field] }}</td>
          </template>
        </tr>
      </tbody>
    </table>
  </div>
</template>

<script>
import BIcon from './Icon.vue'
export default {
  name: 'BlueTable',
  components: {
    BIcon,
  },
  props: {
    columns: {
      type: Array,
      required: true,
    },
    selectedItems: {
      type: Array,
      default: () => [],
    },
    dataSource: {
      type: Array,
      required: true,
      // 判断是否每一项都有ID
      validator(arr) {
        return !(arr.filter((item) => item.id === undefined).length > 0)
      },
    },
    indexIsVisable: {
      type: Boolean,
      default: true,
    },
    bordered: {
      type: Boolean,
      default: false,
    },
    tight: {
      type: Boolean,
      defaut: false,
    },
    striped: {
      type: Boolean,
      default: true,
    },
    orderBy: {
      type: Object,
      default: () => ({}),
    },
  },
  data() {
    return {
      up: true,
    }
  },
  methods: {
    onSlecteItem(row, index, e) {
      let selected = e.target.checked
      let selectedItemsCopy = JSON.parse(JSON.stringify(this.selectedItems))
      // console.log(this.selectedItems, this.selectedItemsCopy, selected, row)
      if (selected) {
        selectedItemsCopy.push(row)
      } else {
        selectedItemsCopy = selectedItemsCopy.filter((i) => i.id !== row.id)
      }
      this.$emit('update:selectedItems', selectedItemsCopy)
    },
    onSelectedAllItems(e) {
      let selected = e.target.checked
      if (selected) {
        this.$emit('update:selectedItems', this.dataSource)
      } else {
        this.$emit('update:selectedItems', [])
      }
    },
    inSelectedItems(row) {
      return this.selectedItems.filter((i) => i.id === row.id).length > 0
    },
    changeOrderBy(key) {
      const copy = JSON.parse(JSON.stringify(this.orderBy))
      const oldVal = copy[key]
      if (oldVal === 'asc') {
        copy[key] = 'desc'
      } else if (oldVal === 'desc') {
        copy[key] = true
      } else {
        copy[key] = 'asc'
      }
      this.$emit('update:orderBy', copy)
    },
  },
  watch: {
    selectedItems() {
      if (this.selectedItems.length >= 1 && this.selectedItems.length < this.dataSource.length) {
        this.$refs.selectAll.indeterminate = true
      } else {
        this.$refs.selectAll.indeterminate = false
      }
    },
  },
  computed: {
    areAllItemsSelected() {
      const ds = this.dataSource.map((item) => item.id).sort()
      const sitems = this.selectedItems.map((item) => item.id).sort()
      if (ds.length !== sitems.length) {
        return false
      }
      let equal = true
      let n = ds.length - 1
      while (n > 0) {
        if (ds[n] !== sitems[n]) {
          equal = false
          break
        }
        n--
      }
      return equal
    },
  },
}
</script>

